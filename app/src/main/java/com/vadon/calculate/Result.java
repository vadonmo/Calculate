public class Result {
    private String leftR;
    private String rightR;
    private String leftH;
    private String rightH;
    private String centerH;
    private String msg;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getLeftR() {
        return leftR;
    }

    public void setLeftR(String leftR) {
        this.leftR = leftR;
    }

    public String getRightR() {
        return rightR;
    }

    public void setRightR(String rightR) {
        this.rightR = rightR;
    }

    public String getLeftH() {
        return leftH;
    }

    public void setLeftH(String leftH) {
        this.leftH = leftH;
    }

    public String getRightH() {
        return rightH;
    }

    public void setRightH(String rightH) {
        this.rightH = rightH;
    }

    public String getCenterH() {
        return centerH;
    }

    public void setCenterH(String centerH) {
        this.centerH = centerH;
    }
}
